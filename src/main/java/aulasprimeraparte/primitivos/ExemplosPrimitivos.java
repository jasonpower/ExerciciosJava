package aulasprimeraparte.primitivos;

public class ExemplosPrimitivos {

    public static void main(String[] args){

        int ano = 2020;
        System.out.println(ano);

        int mes = 0;
        mes = 03;
        System.out.println(mes);

        boolean estaNaHoraDoIntevalo = false;
        System.out.println(estaNaHoraDoIntevalo);

        float valorTaxaSelic = 4.5F;
        System.out.println(valorTaxaSelic);

        double valorImpostoAnual = 23123.22D;
        System.out.println(valorImpostoAnual);

        System.out.println("Valor taxa" +valorTaxaSelic
                            + " Valor imposto:" +valorImpostoAnual);

    }

}
